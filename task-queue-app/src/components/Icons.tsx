// Task type icon component
export function TaskTypeIcon({
  type,
  className = '',
  size,
}: {
  type: string;
  className?: string;
  size?: number;
}) {
  const iconClass = `${className}`;

  switch (type) {
    case 'copy':
      return <CopyIcon className={iconClass} size={size} />;
    case 'zip':
      return <ArchiveIcon className={iconClass} size={size} />;
    case 'tar':
      return <ArchiveIcon className={iconClass} size={size} />;
    case 'transcode':
      return <VideoIcon className={iconClass} size={size} />;
    default:
      return <FileIcon className={iconClass} size={size} />;
  }
}
